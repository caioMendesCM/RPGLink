package com.example.rpglink.models;

public class LastCampCard {

    private String rpgName, campName, playerNun, characterName, characterNvl, pvStat, higherStat1, higherStat2, higherStat3;
    private int rpgPic;

    public LastCampCard() {
    }

    public String getRpgName() {
        return rpgName;
    }

    public void setRpgName(String rpgName) {
        this.rpgName = rpgName;
    }

    public String getCampName() {
        return campName;
    }

    public void setCampName(String campName) {
        this.campName = campName;
    }

    public String getPlayerNun() {
        return playerNun;
    }

    public void setPlayerNun(String playerNun) {
        this.playerNun = playerNun;
    }

    public String getCharacterName() {
        return characterName;
    }

    public void setCharacterName(String characterName) {
        this.characterName = characterName;
    }

    public String getCharacterNvl() {
        return characterNvl;
    }

    public void setCharacterNvl(String characterNvl) {
        this.characterNvl = characterNvl;
    }

    public String getPvStat() {
        return pvStat;
    }

    public void setPvStat(String pvStat) {
        this.pvStat = pvStat;
    }

    public String getHigherStat1() {
        return higherStat1;
    }

    public void setHigherStat1(String higherStat1) {
        this.higherStat1 = higherStat1;
    }

    public String getHigherStat2() {
        return higherStat2;
    }

    public void setHigherStat2(String higherStat2) {
        this.higherStat2 = higherStat2;
    }

    public String getHigherStat3() {
        return higherStat3;
    }

    public void setHigherStat3(String higherStat3) {
        this.higherStat3 = higherStat3;
    }

    public int getRpgPic() {
        return rpgPic;
    }

    public void setRpgPic(int rpgPic) {
        this.rpgPic = rpgPic;
    }
}